export function getBackstory(breed, backstoryId) {
  const stories = BACKSTORIES[breed];
  if (!stories) return null;
  return stories[backstoryId % stories.length];
}

export function getRandomBackstoryId(breed) {
  const stories = BACKSTORIES[breed];
  if (!stories) return 0;
  return Math.floor(Math.random() * stories.length);
}
